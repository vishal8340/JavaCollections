package java_project;

public class EvenAndOdd {

	public static void main(String[] args) {
		
		int number = 10;
		
		for(int i=1;i<=number;i++)
		{
			
			if(i%2==0)
			{

				System.out.println("Even numbers are : "+i);
				
			}
			else
			{
				
				System.out.println("Odd numbers are : "+i);
			}
		}

	}

}
